# immutable data types (can not change)
# similar to lists, but you can't add/remove items

tuple_1 = (5, 6, 8, 9)
tuple_2 = (8, "f", "string", 55)

# a tuple with one element
tuple_3 = ("gh",)

# empty tuple
tuple_4 = ()

# built-in functions for tuples
# number of elements in tuple
print(len(tuple_1))

# converting a list into tuple or CASTING
# tuple(list)

# max value
print(max(tuple_1))

