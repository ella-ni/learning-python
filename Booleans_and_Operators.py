# Booleans are built-in data type. 
# TRUE OR FALSE

# Boolean operators are
#   AND: requires both values to be TRUE for result to be TRUE
#   OR: requires one of the values  to be TRUE for the result to be TRUE
#   NOT: negates the value that follows it

print(True and True)
print(True and False)
#  with OR Python won't even look at the second option, if the first one is already TRUE
print(True or False) 
print(True or True)
print(not True)
print(not False)
print(True and not False)

